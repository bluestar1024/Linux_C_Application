void show_char(unsigned int x, unsigned int y, unsigned char *ch, \
                    unsigned int w, unsigned int h, unsigned int color)
{
    unsigned short rgb565_color = argb8888_to_rgb565(color);
    unsigned int base = 0;
    unsigned int end_x = 0, end_y = 0;
    unsigned int w_bytes = 0;
    int k = 0;

    w_bytes = w / 8;
    if(0 != w % 8)
        w_bytes++;
    
    if((x >= width) || (y >= height))
        return ;
    if((w < 1) || (h < 1))
        return ;

    end_x = x + w - 1;
    if(end_x >= width)
        end_x = width - 1;
    end_y = y + h - 1;
    if(end_y >= height)
        end_y = height - 1;
    
    w = end_x - x + 1;
    h = end_y - y + 1;

    base = y * width + x;
    for(y = 0; y < h; y++, base += width)
    {
        for(x = 0; x < w; x++)
        {
            k = x / 8;
            if(*(ch + y * w_bytes + k) & (0x1 << (x % 8)))
                screen_base[base + x] = rgb565_color;
        }
    }
}

